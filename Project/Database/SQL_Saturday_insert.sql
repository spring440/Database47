CREATE PROCEDURE inserAlltPresentation(
       @pTitle varchar(255),
	   @pSpeaker varchar(50),
	   @pLevel varchar(50),
	   @pEvent varchar(50))
  AS
    IF EXISTS (SELECT TOP 1 ID FROM Event WHERE city=@pEvent)
    BEGIN TRY
	   INSERT INTO Presentation
	   (title,difficulty)
	   VALUES
	   (@pTitle,@pLevel)
	   INSERT INTO Person
	   (name)
	   VALUES
	   (@pSpeaker)
	   INSERT INTO PresentationEvent
	   (Presentation_id, Event_id)
	   VALUES
       (SCOPE_IDENTITY(), (SELECT TOP 1 ID FROM Event WHERE
		city=@pEvent));
	END TRY
	BEGIN CATCH
		SELECT ERROR_MESSAGE() AS ErrorMessage;
	END CATCH
	ELSE
	BEGIN
	  PRINT(@PEVENT + 'City not found');
	END


